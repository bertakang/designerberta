          <li>Struggle with language learning due to low literacy in Khmer and challenges in acquiring typing and computer skills.</li>
      </ul>
      <p>
          By identifying these factors, I could ensure the app would be designed with their unique needs in mind, particularly considering issues such as mobile-first accessibility, low literacy support, and an intuitive user experience.
      </p>
  
     
  
  
