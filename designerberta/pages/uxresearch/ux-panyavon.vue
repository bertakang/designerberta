          <li><strong>Cultural Relevance</strong>: Including Cambodian holidays, local idioms, and cultural references in the curriculum made the learning experience more engaging and relatable.</li>
          <li><strong>TESOL Principles</strong>: Aligning with TESOL standards allowed for structured, progressive learning pathways that could cater to various learning levels.</li>
      </ul>
  
